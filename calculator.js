// Define the numbers and the operation here
let integer1 = 3;
let integer2 = 5;
let operation = "/";

// Write if-statements here
if (operation === "+") {

    console.log(integer1 + " + " + integer2 + " = " + (integer1+integer2));

} else if (operation === "*"){

    console.log(integer1 + " * " + integer2 + " = " + (integer1*integer2));;

} else if (operation === "-") {


    console.log(integer1 + " - " + integer2 + " = " + (integer1-integer2));

} else if (operation === "/") {

    console.log(integer1 + " / " + integer2 + " = " + (integer1/integer2));
}